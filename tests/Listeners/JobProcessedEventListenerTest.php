<?php

namespace Tests\Listeners;

use Illuminate\Queue\Events\JobProcessed;
use Tests\TestCase;
use App\Listeners\JobProcessedEventListener;

class JobProcessedEventListenerTest extends TestCase
{
    public function testHandle()
    {
        $client = \Mockery::mock('Aws\CloudWatch\CloudWatchClient')
            ->shouldReceive('putMetricData')
            ->andReturn([])
            ->once()
            ->getMock();

        $listener = new JobProcessedEventListener($client);
        $listener->handle(new JobProcessed('does', 'not', 'matter'));
    }

    public function tearDown()
    {
        if ($container = \Mockery::getContainer()) {
            $this->addToAssertionCount($container->mockery_getExpectationCount());
        }
        \Mockery::close();
        parent::tearDown(); // TODO: Change the autogenerated stub
    }
}
